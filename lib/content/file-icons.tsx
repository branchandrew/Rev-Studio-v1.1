// Helper function to get the appropriate icon based on file type
export function getFileIcon(fileType: string) {
  switch (fileType.toLowerCase()) {
    case "pdf":
      return <PdfIcon />
    case "docx":
    case "doc":
      return <WordIcon />
    case "pptx":
    case "ppt":
      return <PowerPointIcon />
    case "mp4":
    case "mov":
    case "avi":
    case "webm":
      return <VideoIcon />
    case "jpg":
    case "jpeg":
    case "png":
    case "gif":
    case "webp":
      return <ImageIcon />
    case "txt":
      return <TxtIcon />
    case "zip":
    case "rar":
    case "7z":
      return <ZipIcon />
    default:
      return <GenericFileIcon />
  }
}

// Function to determine file type from file name
export function getFileTypeFromName(fileName: string): string {
  const extension = fileName.split(".").pop()?.toLowerCase() || ""
  return extension
}
